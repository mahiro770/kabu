st.markdown("## 🤖 AIモデルについて")
with st.container(border=True, key="seccard_usage_models"):
    st.markdown("""
AI分析・スクリーニングでは、サイドバーの「AIモデル」から使用するモデルを選べます。

| モデル | 特徴 |
|------|------|
| 🦙 Ollama | 自分のPCで動かすローカルモデル。完全無料・回数制限なしですが、`ollama serve` の起動が必要で、デプロイ版（クラウド）では利用できません |
| ✨ Gemini | Googleのクラウドモデル。無料枠がありますが、1日あたりのリクエスト数に上限があります |
| ⚡ Groq | 高速な推論が特徴のクラウドモデル。こちらも無料枠がありますが上限があります |

無料枠には限りがあるため、モデルによって「現在エラーが出やすい/出にくい」が
変わることがあります。エラーが出た場合は、別のモデルに切り替えてお試しください。
""")

